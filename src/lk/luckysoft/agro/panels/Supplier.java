    private void loadSuppliers() {
        try {
            int i = 0 ;
            
            ResultSet rs = MySQL.search("SELECT * FROM `supplier` INNER JOIN `company` ON `supplier`.`company_id` = `company`.`id` ");
            DefaultTableModel suplierDetails = (DefaultTableModel) jTable1.getModel();
            suplierDetails.setRowCount(0);
            

            while (rs.next()) {
                i++;
                Vector<String> supplierVec = new Vector<>();
                supplierVec.add(rs.getString("id"));
                supplierVec.add(rs.getString("fname") + " " + rs.getString("lname"));
                supplierVec.add(rs.getString("email"));
                supplierVec.add(rs.getString("mobile"));
                supplierVec.add(rs.getString("company_name"));
                suplierDetails.addRow(supplierVec);
            }
            jLabel7.setText(String.valueOf(i));

        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    private void loadcompany() {
        try {
            Vector companyvec = new Vector();
            companyvec.add("Select Company");

            ResultSet companyrs = MySQL.search("SELECT * FROM `company`");
            while (companyrs.next()) {
                companyvec.add(companyrs.getString("company_name"));
                companyMap.put(companyrs.getString("company_name"), companyrs.getString("id"));
            }

            jComboBox1.setModel(new DefaultComboBoxModel<>(companyvec));

        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

