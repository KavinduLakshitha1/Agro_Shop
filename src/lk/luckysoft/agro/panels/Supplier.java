    private void roundedButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundedButton1ActionPerformed
        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String email = jTextField3.getText();
        String mobile = jTextField4.getText();
        String selectedCompany = (String) jComboBox1.getSelectedItem();
        String company = companyMap.get(String.valueOf(selectedCompany));

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Last Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Email Address", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!email.matches(Validation.EMAIL_VALIDATION.validate())) {
            JOptionPane.showMessageDialog(this, "Please enter valid email", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (mobile.matches(Validation.MOBILE_VALIDATION.validate())) {

            JOptionPane.showMessageDialog(this, "Please enter valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.length() > 10 || mobile.length() < 10) {

            JOptionPane.showMessageDialog(this, "Please enter valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (selectedCompany.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please select your company", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `supplier` WHERE `mobile` = '" + mobile + "' && email='" + email + "'");
                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "User already exist please check mobile number and email", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `supplier`(`fname`,`lname`,`mobile`,`email`,`company_id`) VALUES('" + fname + "','" + lname + "','" + mobile + "','" + email + "','" + company + "') ");
                    loadSuppliers();
                    reset();
                }
            } catch (SQLException e) {
                throw new RuntimeException(e);
            }

        }
    }//GEN-LAST:event_roundedButton1ActionPerformed

    private void roundedButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundedButton2ActionPerformed
        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String email = jTextField3.getText();
        String mobile = jTextField4.getText();
        String selectedCompany = (String) jComboBox1.getSelectedItem();
        String company = companyMap.get(String.valueOf(selectedCompany));

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Last Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (selectedCompany.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select your company", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `supplier` WHERE `mobile` = '" + mobile + "' && email='" + email + "'");
                if (rs.next()) {
                    MySQL.iud("UPDATE `supplier` SET `fname`='" + fname + "' , `lname`='" + lname + "' , `company_id`='" + company + "' WHERE `mobile` = '" + mobile + "' && email='" + email + "'");
                    loadSuppliers();
                    reset();

                    roundedButton1.setEnabled(true);
                    roundedButton2.setEnabled(false);
                    roundedButton3.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Supplier not found please check again.", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (SQLException e) {
                throw new RuntimeException(e);
            }

        }

    }//GEN-LAST:event_roundedButton2ActionPerformed

