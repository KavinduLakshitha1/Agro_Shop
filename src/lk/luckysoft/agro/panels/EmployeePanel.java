    private void loadEmployee() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `user` WHERE `user_type_id`='2'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector<String> vect = new Vector<>();

                vect.add(rs.getString("id"));
                vect.add(rs.getString("fname") + " " + rs.getString("lname"));
                vect.add(rs.getString("email"));
                vect.add(rs.getString("mobile"));
                vect.add(rs.getString("password"));
                dtm.addRow(vect);
            }
            jLabel3.setText(String.valueOf(dtm.getRowCount()));
        } catch (SQLException e) {
//            throw new RuntimeException(e);
            log.log.warning(e.toString());
        }
    }

