    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();

            String id = (String) jTable1.getValueAt(selectedRow, 0);
            String name = (String) jTable1.getValueAt(selectedRow, 1);
            String email = (String) jTable1.getValueAt(selectedRow, 2);
            String mobile = (String) jTable1.getValueAt(selectedRow, 3);
            String password = (String) jTable1.getValueAt(selectedRow, 4);

            String[] splittext = name.split(" ");
            String fname = splittext[0];
            String lname = splittext[1];

            EmployeeRegistration er = new EmployeeRegistration(dashboard, true, this);
            er.setLocationRelativeTo(dashboard);

            er.getjTextField1().setText(id);
            er.getjTextField1().setEnabled(false);
            er.getjTextField2().setText(fname);
            er.getjTextField3().setText(lname);
            er.getjTextField4().setText(email);

            er.getjTextField5().setText(mobile);
            er.getjTextField6().setText(password);

            er.setVisible(true);

        }
    }//GEN-LAST:event_jTable1MouseClicked


