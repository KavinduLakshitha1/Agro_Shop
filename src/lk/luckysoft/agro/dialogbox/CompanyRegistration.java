    private void roundedButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundedButton1ActionPerformed
        String Name = jTextField3.getText();
        String tel_no = jTextField2.getText();
        
        if(Name.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter compnay name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(tel_no.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter mobile numberr", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (tel_no.matches(Validation.MOBILE_VALIDATION.validate())) {

            JOptionPane.showMessageDialog(this, "Please enter valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (tel_no.length() > 10 || tel_no.length() < 10) {

            JOptionPane.showMessageDialog(this, "Mobile number must contain only 10 degits.", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            MySQL.iud("INSERT INTO `company` (`company_name`,`tel_no`) VALUES('"+Name+"','"+tel_no+"')");
            JOptionPane.showMessageDialog(this, "Successfully company added.", "Warning", JOptionPane.WARNING_MESSAGE);
            loadComapany();
        }
    }//GEN-LAST:event_roundedButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();
            String id = (String) jTable1.getValueAt(selectedRow, 0);
            String name = (String) jTable1.getValueAt(selectedRow, 1);
            String tel_no = (String) jTable1.getValueAt(selectedRow, 2);

            

            jTextField1.setText(id);
            jTextField3.setText(name);
            jTextField2.setText(tel_no);
            
            roundedButton1.setEnabled(false);
            roundedButton2.setEnabled(true);
            roundedButton3.setEnabled(true);
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

