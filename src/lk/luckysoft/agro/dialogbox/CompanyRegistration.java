    private void loadComapany() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `company`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector<String> vect = new Vector<>();

                vect.add(rs.getString("id"));
                vect.add(rs.getString("company_name"));
                vect.add(rs.getString("tel_no"));
//                vect.add(rs.getString("hotline"));
//                vect.add(rs.getString("email"));
                dtm.addRow(vect);
            }
            jLabel7.setText(String.valueOf(dtm.getRowCount()));
        } catch (SQLException e) {
            throw new RuntimeException(e);
        }

    }

